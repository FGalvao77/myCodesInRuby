# Seu primeiro "Hello World"

# 1. Para fazer seu primeiro programa crie um arquivo dentro da sua máquina ou container ruby "hello.rb"
	# Obs.: Os arquivos de código ruby possuem a extensão ".rb"

# 2. Abra o arquivo e adicione a seguinte linha.

puts 'Hello, World!' # a função "puts" imprime algo na tela.

# 3. Para executar o programa criado, acesse o Terminal ou SSH Console e rode o comando.

# ruby hello.rb

	# A instrução puts vai imprimir a mensagem 'Hello, World!' toda vez que o programa for excutado.


# Podemos também imprimir algo usando as funções "p" e "print".
	# Como segue os exemplos abaixo:
	
