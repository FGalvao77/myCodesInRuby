# Operadores Matemáticos
# para resolver operações matemáticas no ruby 
# contamos com a seguinte lista de operadores aritméticos:

# + (Adição)
# – (Subtração)
# * (Multiplicação)
# / (Divisão)
# % (Módulo)
# ** (Expoente)

# adição
p -5 + 8

# subtração
p 9 - 13

# multiplicação
p 7 * 5

# divisão
p 21 / 3

# módulo - retorna o resto da divisão
p 10 % 3

# exponenciação
p 2 ** 3

# podemos realizar operações matemáticas usando variáveis
a = 5
b = 2

